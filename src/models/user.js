export default class User {
  constructor(
    salary,
    id,
    firstName,
    lastName,
    city,
    age,
    type,
    creditCardNumber
  ) {
    this.id = id;
    this.firstName = firstName;
    this.lastName = lastName;
    this.city = city;
    this.age = age;
    this.type = type;
    this.creditCardNumber = creditCardNumber;
    this.salary = salary;
  }
}

